# Импорт класса datetime из модуля datetime для работы с датой и временем
from datetime import datetime

# Импорт функции sqrt из модуля math для вычисления квадратного корня
from math import sqrt

def main(**kwargs):
    """
    Функция вычисляет расстояние от начала координат до точек, переданных в параметрах.
    
    Args:
        **kwargs: Произвольное количество именованных аргументов, 
                 где значения - списки с двумя числами [x, y]
    """
    # Перебор всех элементов словаря kwargs
    for key in kwargs.items():
        # key[0] - имя параметра, key[1] - список с координатами [x, y]
        # Вычисление расстояния по формуле: sqrt(x² + y²)
        result = sqrt(key[1][0] ** 2 + key[1][1] ** 2)
        # Вывод результата в консоль
        print(result)

# Точка входа - код выполняется только при прямом запуске файла
if __name__ == '__main__':
    # Запись текущего времени в переменную start_time
    start_time = datetime.now()
    
    # Вызов функции main с передачей нескольких точек в виде именованных аргументов
    main(
        one=[10, 3],      # Точка с координатами (10, 3)
        two=[5, 4],       # Точка с координатами (5, 4)
        three=[15, 13],   # Точка с координатами (15, 13)
        four=[93, 53],    # Точка с координатами (93, 53)
        five=[133, 15]    # Точка с координатами (133, 15)
    )
    
    # Вычисление времени выполнения программы
    time_costs = datetime.now() - start_time
    # Вывод времени выполнения
    print(f"Время выполнения программы - {time_costs}")
